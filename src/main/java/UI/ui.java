    private void resetbtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_resetbtnActionPerformed
        // TODO add your handling code here:
           try{
            int id = Integer.parseInt(idfield.getText());
            String name = namefield.getText();
            float control = Float.parseFloat(controlfield.getText());
            float intero = Float.parseFloat(interofield.getText());
            dao.updateEtudiant(new etudiant( id ,name , intero , control));
            idfield.setText("");
        namefield.setText("");
        controlfield.setText("");
        interofield.setText("");
            remplireTable();
        }catch(Exception e){
            JOptionPane.showMessageDialog(null, "Please check the data inputs if not check the database connection :"+e); 
          
        }
    }//GEN-LAST:event_resetbtnActionPerformed

