    void remplireTable(){
        etudiants = (ArrayList<etudiant>) dao.getEtudiant();
        accepter = (ArrayList<etudiant>) dao.getMoyB10();
        ajourner = (ArrayList<etudiant>) dao.getMoyenU10();
        model2 = new DefaultTableModel();
        model1= new DefaultTableModel();
        model3= new DefaultTableModel();
        Object[] colomun = {"ID" , "Nom" , "Control" , "Interogation" };
        Object[] row = new Object[5];
        model1.setColumnIdentifiers(colomun);
        model2.setColumnIdentifiers(colomun);
        model3.setColumnIdentifiers(colomun);
        jTable2.setModel(model2);
        jTable1.setModel(model1);
        jTable3.setModel(model3);
        for (etudiant e : etudiants){
            // Printing the elements of ArrayList
            row[0] = e.getId();
            row[1] = e.getName();
            row[2] = e.getControl();
            row[3] = e.getIntero();
            
            model2.addRow(row);

    }
        for (etudiant e : ajourner){
            // Printing the elements of ArrayList
            row[0] = e.getId();
            row[1] = e.getName();
            row[2] = e.getControl();
            row[3] = e.getIntero();
            
            model3.addRow(row);

    }
        for (etudiant e : accepter){
            // Printing the elements of ArrayList
            row[0] = e.getId();
            row[1] = e.getName();
            row[2] = e.getControl();
            row[3] = e.getIntero();
            
            model1.addRow(row);

    }
    }
